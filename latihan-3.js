document.write('hasil perhitungan suhu celcius ke kelvin adalah :' + kelvin + '<br>');
document.write('hasil perhitungan suhu celcius ke fahrenheit adalah :' + fahrenheit + '<br>');
document.write('hasil perhitungan suhu celcius ke reamur adalah :' + reamur + '<br>');